package com.gionee.ssp.vo.preload;

import java.util.List;

/**
 * 
 * @ClassName PreloadResponseVo
 * @Desc {预加载返回vo}
 * @author zhengk
 * @date Apr 6, 2017
 */

public class PreloadResponseVo {
	/** 错误码 **/
	private String code;

	/** 错误描述 **/
	private String msg;

	/** 创意，返回多条 **/
	private List<PreloadImgInfoVo> datas;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public List<PreloadImgInfoVo> getDatas() {
		return datas;
	}

	public void setDatas(List<PreloadImgInfoVo> datas) {
		this.datas = datas;
	}

}
