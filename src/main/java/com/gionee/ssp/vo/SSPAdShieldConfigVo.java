package com.gionee.ssp.vo;

import java.util.List;

import com.gionee.common.vo.BaseVo;

/** 
 * @describe 广告屏蔽配置
 * @author  XuLei 
 * @date 2017年8月15日 上午11:00:00  
 */
public class SSPAdShieldConfigVo extends BaseVo{

	/** 用户屏蔽天数 **/
	private int days;
	
	/** imei屏蔽 **/
	private List<String> imei_list;
	
	/** 机型屏蔽 **/
	private List<String> model_list;

	/**新用户的白名单列表:在列表中指定的imei用户不受新用户的限制,依旧可以请求广告**/
	private List<String> new_user_white_list;

	public int getDays() {
		return days;
	}

	public void setDays(int days) {
		this.days = days;
	}

	public List<String> getImei_list() {
		return imei_list;
	}

	public void setImei_list(List<String> imei_list) {
		this.imei_list = imei_list;
	}

	public List<String> getModel_list() {
		return model_list;
	}

	public void setModel_list(List<String> model_list) {
		this.model_list = model_list;
	}

	public List<String> getNew_user_white_list() {
		return new_user_white_list;
	}

	public void setNew_user_white_list(List<String> new_user_white_list) {
		this.new_user_white_list = new_user_white_list;
	}
}
