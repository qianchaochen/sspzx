package com.gionee.ssp.vo;

import com.gionee.common.vo.BaseVo;

/**
 * @author dingyw
 *
 * 2017年4月19日
 */
public class PercentItemVo extends BaseVo{
	
	/**
	 * adx的名字
	 */
	private String adx_name;
	
	/**
	 * 流量排名得分(算法需要，用来计算概率)
	 */
	private Integer score;
	
	public PercentItemVo(String adx_name,Integer score){
		this.adx_name=adx_name;
		this.score=score;
	}

	public String getAdx_name() {
		return adx_name;
	}

	public void setAdx_name(String adx_name) {
		this.adx_name = adx_name;
	}

	public Integer getScore() {
		return score;
	}

	public void setScore(Integer score) {
		this.score = score;
	}
	
	
	
}
