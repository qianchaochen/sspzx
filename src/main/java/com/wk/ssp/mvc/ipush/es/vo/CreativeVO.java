package com.wk.ssp.mvc.ipush.es.vo;

/**
 *
 * 创意对象
 *
 */
public class CreativeVO {

    private int creative_id; // 创意id
    private int w; // 创意宽
    private int h; // 创意高
    private String img_url; // 创意的地址
    private String title; // 主标题
    private String subtitle; // 子标题
    private int title_len; // 标题长度
    private int sub_title_len; // 子标题长度
    private String source; // 来源
    private String icon; // 图标url
    private String iconaction; // 行为图标url

    public int getCreative_id() {
        return creative_id;
    }

    public void setCreative_id(int creative_id) {
        this.creative_id = creative_id;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public String getImg_url() {
        return img_url;
    }

    public void setImg_url(String img_url) {
        this.img_url = img_url;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public void setSubtitle(String subtitle) {
        this.subtitle = subtitle;
    }

    public int getTitle_len() {
        return title_len;
    }

    public void setTitle_len(int title_len) {
        this.title_len = title_len;
    }

    public int getSub_title_len() {
        return sub_title_len;
    }

    public void setSub_title_len(int sub_title_len) {
        this.sub_title_len = sub_title_len;
    }

    /**
     * @return the source
     */
    public String getSource() {
        return source;
    }

    /**
     * @param source the source to set
     */
    public void setSource(String source) {
        this.source = source;
    }

    /**
     * @return the icon
     */
    public String getIcon() {
        return icon;
    }

    /**
     * @param icon the icon to set
     */
    public void setIcon(String icon) {
        this.icon = icon;
    }

    /**
     * @return the iconaction
     */
    public String getIconaction() {
        return iconaction;
    }

    /**
     * @param iconaction the iconaction to set
     */
    public void setIconaction(String iconaction) {
        this.iconaction = iconaction;
    }

}
