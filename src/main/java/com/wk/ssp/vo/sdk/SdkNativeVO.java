package com.wk.ssp.vo.sdk;

import java.util.List;

import com.gionee.common.vo.BaseVo;

/**
 * @description:
 */
public class SdkNativeVO extends BaseVo{

	/** 主标题 **/
	private String title;
	
	/** 原生广告类型。1：组图，2：小图，3：大图 **/
	private int type;
	
	/** 子标题 **/
	private String sub_title;
	
	/** 推广来源 **/
	private String source;
	
	/** 创意图片url **/
	private List<String> imgurl;
	
	/** 创意图片md5 **/
	private List<String> imgmd5;
	
	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSub_title() {
		return sub_title;
	}

	public void setSub_title(String sub_title) {
		this.sub_title = sub_title;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public List<String> getImgurl() {
		return imgurl;
	}

	public void setImgurl(List<String> imgurl) {
		this.imgurl = imgurl;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}
	
	public List<String> getImgmd5() {
		return imgmd5;
	}

	public void setImgmd5(List<String> imgmd5) {
		this.imgmd5 = imgmd5;
	}
}
