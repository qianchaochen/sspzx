package com.wk.ssp.vo;

import java.util.List;

import com.gionee.common.vo.BaseVo;

/**
 * @description: 广告位填充信息
 */
public class FillBannerVO extends BaseVo{

	/** 广告位宽 */
	private int w;
	
	/** 广告位高 */
	private int h;
	
	/**  广告位位置 0:未知，4：头部，5：底部，6：侧边栏，7：全屏  */
	private int pos;
	
	/** 横幅请求类型.0:纯图片，1：图文 **/
	private List<Integer> type;
	
	/** 图文类广告背景色值 **/
	private String bg_color;
	
	/** 图文类广告文字色值 **/
	private String tx_color;

	public int getW() {
		return w;
	}

	public void setW(int w) {
		this.w = w;
	}

	public int getH() {
		return h;
	}

	public void setH(int h) {
		this.h = h;
	}

	public int getPos() {
		return pos;
	}

	public void setPos(int pos) {
		this.pos = pos;
	}

	public List<Integer> getType() {
		return type;
	}

	public void setType(List<Integer> type) {
		this.type = type;
	}

	public String getBg_color() {
		return bg_color;
	}

	public void setBg_color(String bg_color) {
		this.bg_color = bg_color;
	}

	public String getTx_color() {
		return tx_color;
	}

	public void setTx_color(String tx_color) {
		this.tx_color = tx_color;
	}
}
